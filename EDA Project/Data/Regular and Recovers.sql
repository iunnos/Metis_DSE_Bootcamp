SELECT a.*
FROM mta_data a
JOIN (SELECT [C/A], UNIT, SCP, STATION, DATE, TIME, COUNT(*)
FROM mta_data
GROUP BY [C/A], UNIT, SCP, STATION, DATE, TIME
HAVING COUNT(*)>1) b
ON a.[C/A] = b.[C/A]
AND a.UNIT = b.UNIT
AND a.SCP = b.SCP
AND a.STATION = b.STATION
AND a.DATE = b.DATE
AND a.TIME = b.TIME
ORDER BY a.[C/A], a.UNIT, a.SCP, a.STATION, a.DATE, a.TIME;